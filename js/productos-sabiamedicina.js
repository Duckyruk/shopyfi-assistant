window.buscarProductosPorBeneficio = function(keywords) {
    const resultados = [];
    const keywordsLower = keywords.toLowerCase();
    
    for (const categoria in PRODUCTOS_SABIAMEDICINA) {
        PRODUCTOS_SABIAMEDICINA[categoria].forEach(producto => {
            if (producto.nombre.toLowerCase().includes(keywordsLower)) {
                resultados.push({...producto, categoria});
                return;
            }
            if (producto.descripcion.toLowerCase().includes(keywordsLower)) {
                resultados.push({...producto, categoria});
                return;
            }
            const tienebeneficio = producto.beneficios.some(beneficio => 
                beneficio.toLowerCase().includes(keywordsLower)
            );
            if (tienebeneficio) {
                resultados.push({...producto, categoria});
            }
        });
    }
    return resultados;
};

window.obtenerProductosCategoria = function(categoria) {
    return PRODUCTOS_SABIAMEDICINA[categoria] || [];
};

